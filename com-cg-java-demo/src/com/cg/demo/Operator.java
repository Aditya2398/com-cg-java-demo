package com.cg.demo;

public class Operator {
	public static void main(String[] args) {
		
		int i = 20, j = 6, k;
		
		k = i + j;
		System.out.println(k);

		k = i - j;
		System.out.println(k);

		k = i * j;
		System.out.println(k);

		k = i / j;
		System.out.println(k);

		k = i % j;
		System.out.println(k);
	}

}
